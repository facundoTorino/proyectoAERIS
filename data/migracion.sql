CREATE DATABASE gamers_shroud;

use gamers_shroud;

CREATE TABLE juegos (
	ID_JUEGO INT(2) UNSIGNED AUTO_INCREMENT PRIMARY KEY,
	NOMBRE_JUEGO VARCHAR(50) NOT NULL,
	GENERO_JUEGO VARCHAR(50) NOT NULL,
	DESC_JUEGO VARCHAR(500) NOT NULL,
	SO_M VARCHAR(50) NOT NULL,
	PROCESADOR_M VARCHAR(50) NOT NULL,
	RAM_M VARCHAR(50) NOT NULL,
	GRAFICA_M VARCHAR(50) NOT NULL,
	DIRECTX_M VARCHAR(50) NOT NULL,
	SO_R VARCHAR(50) NOT NULL,
	PROCESADOR_R VARCHAR(50) NOT NULL,
	RAM_R VARCHAR(50) NOT NULL,
	GRAFICA_R VARCHAR(50) NOT NULL,
	DIRECTX_R VARCHAR(50) NOT NULL,
	DESC_RED VARCHAR(50),
	created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);